const first = 1;
const second = true;

//doubole equal check only equality of values
if(first == second){
    console.log("condition is true");
}
else {
    console.log("condition is false")
}

//triple equal check equality of value and type
if(first === second){
    console.log("condition is true");
}
else {
    console.log("condition is false")
}

//so for compare we will always use ===